# Get inputs and return sum
def sum(num1, num2):
    return num1 + num2

# Get inputs and return subtraction
def subtraction(num1, num2):
    return num1 - num2

# Get inputs and return division
def division(num1, num2):
    return num1 / num2

# Get inputs and return multiplication
def multiplication(num1, num2):
    return num1 * num2
    